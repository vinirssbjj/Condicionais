let dia_da_semana = 5;

if (dia_da_semana <= 0 || dia_da_semana >= 8){
    console.log("Dia da semana INVÁLIDO");
} else if (dia_da_semana == 1){
    console.log("Domingo");
} else if (dia_da_semana == 2){
    console.log("Segunda");
} else if (dia_da_semana == 3){
    console.log("Terça");
} else if (dia_da_semana == 4){
    console.log("Quarta");
} else if (dia_da_semana == 5){
    console.log("Quinta");
} else if (dia_da_semana == 6){
    console.log("Sexta");
} else if (dia_da_semana == 7){
    console.log("Sabado");
}